# Create a set of unique integers
my_set = {1, 2, 3, 4, 5}

# Add an element to the set
my_set.add(6)

# Remove an element from the set
my_set.remove(3)

# Check if an element is in the set
if 4 in my_set:
    print("4 is in the set")
else:
    print("4 is not in the set")

# Loop through the set
for num in my_set:
    print(num)
